VERSION_FILE="/opt/l-panel/VERSION"

###########################################

version() {

    if [[ -f "$VERSION_FILE" ]]; then
        cat "$VERSION_FILE"
    else
        echo "0.0.0"
    fi

}

###########################################

version_string() {

    echo "Version : $(version)"

}

